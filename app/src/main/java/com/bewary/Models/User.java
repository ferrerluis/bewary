package com.bewary.Models;

public class User {
    private int id;
    private String name;
    private String email;
    private String imageURL;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getImageURL() {
        return imageURL;
    }
}
